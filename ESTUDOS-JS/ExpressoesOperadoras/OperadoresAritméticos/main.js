// Operadores Aritiméticos

// Multiplicação *
console.log(3.2 * 5.5)

// divisão /
console.log(12 / 2) 

// soma +
console.log(34 + 67)

// subtração -
console.log(34 - 23)

// resto da divisão %
let resto 
resto = 11 % 3
console.log(resto)

// incremento ++
let increment = 0 
increment++
console.log(increment)

// decremento --
let decrement = 0
console.log(--decrement)

// exponencial **
console.log(3 ** 3)











