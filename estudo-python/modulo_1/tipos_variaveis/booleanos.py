# Condição verdadeira
#- TRUE
# Condição falsa
#- FALSE

# Condicionais
# se(if) condição
# else = se não

if True:
    print("Bloco IF vai ser executado")
else:
    print("Bloco ELSE não será executado")

# Operadores lógicos: and e or

# AND
if True and True:
    print("Bloco será executado")
    
if True and False:
    print("Bloco não será executado")
    
if False and False:
    print("Bloco não será executado")

# OR

if True or False:
    print("Bloco OR vai ser executado")

if False or False:
    print("Bloco OR não vai ser executado")   


# Podemos utilizar os operadores lógicos "and" e "or" para comparar duas entradas e obter uma saída. O operador "and" só retorna verdadeiro se as duas condições forem verdade   
