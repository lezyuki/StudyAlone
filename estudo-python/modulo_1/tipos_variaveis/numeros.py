# Inteiro 
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type ()
print("Tipo de variavel inteiro = ", type(numero_inteiro))
print("Tipo de variavel real = ", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# Subtração -
subtracao = 2 - 1 
print("2 - 1 = ", subtracao)

# multiplicacao
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# divisao
divisao = 4 / 2
print("4 / 2 = ", divisao)

# int
print("valor em inteiro", int(divisao))

# float
print("valor em real", float(multiplicacao))

# modulo %
modulo = 5 % 2
print("resto da divisão = ", modulo) 

# divisao inteiro //
divisaoo = 5 // 2
print("5 // 2 = ", divisaoo)
print("Tipo da variavel do resultado da div = ", type(divisaoo)) 


