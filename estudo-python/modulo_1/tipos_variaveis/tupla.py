# Criando uma tupla de exemplo
minhaTupla = (1, 2, 2, 3, 4)

print("Minha tupla:", minhaTupla)

print("minhaTupla[0]:", minhaTupla[0])
print("minhaTupla[2]:", minhaTupla[3])
print("minhaTupla[-1]:", minhaTupla[-1])


# Método count
contagem = minhaTupla.count(2)
print("Quantidade de vezes que o elemento 2 aparece:", contagem)

indice = minhaTupla.index(3)
print("Indice da primeira ocorrência do elemento 3:", indice)